package Basics;

public class assignmentQ7 {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        boolean isEqual = num1 == num2;
        System.out.println("Are numbers equal " +isEqual);
        boolean isnotEqual = num1 != num2;  //  != shows not equal to
        System.out.println("Are numbers not equal " +isnotEqual);
        boolean isgreater = num1 > num2;
        System.out.println("Are numbers greater than " +isgreater);
        boolean isless = num1 < num2;
        System.out.println("Are numbers less than " +isless);

    }
}
